const Product = require('../models/Product');
const fs = require('fs');
const { generateProductSlug, ensureUniqueSlug, validateSlug } = require('../utils/slugGenerator');
const { autoGenerateProductSEO, validateSEOFields } = require('../utils/seoUtils');
const { generateImageVariants } = require('../utils/imageProcessor');
const apiCache = require('../middleware/apiCacheMiddleware');

// Add a new product
const addProduct = async (req, res) => {
  try {
    console.log('=== ADD PRODUCT REQUEST ===');
    console.log('Request body:', req.body);
    console.log('File:', req.file ? req.file.originalname : 'No file');
    
    const { 
      title, 
      keywords, 
      description, 
      price, 
      categories,
      // SEO Fields
      seoTitle,
      seoDescription,
      imageAlt,
      metaKeywords,
      focusKeyword,
      shortDescription,
      // Featured spotlight controls (optional)
      featuredUntil,
      isSoldOut
    } = req.body;

    console.log('Extracted fields:', { title, keywords, description, price, categories });

    if (!title || !keywords || !description || !price || !categories || !req.file) {
      console.log('Validation failed - missing required fields');
      return res.status(400).json({ message: 'All required fields must be provided (title, keywords, description, price, categories, image)' });
    }

    // Parse arrays from JSON strings
    const parsedKeywords = typeof keywords === 'string' ? keywords.split(',').map(keyword => keyword.trim()) : keywords;
    const parsedCategories = typeof categories === 'string' ? JSON.parse(categories) : categories;
    const parsedMetaKeywords = metaKeywords ? (typeof metaKeywords === 'string' ? metaKeywords.split(',').map(k => k.trim()) : metaKeywords) : [];

    // Prepare basic product data
    const productData = {
      title: title.trim(),
      keywords: parsedKeywords,
      description: description.trim(),
      price: parseFloat(price),
      categories: parsedCategories,
      image: req.file.path,
      // SEO fields (if provided)
      seoTitle: seoTitle?.trim(),
      seoDescription: seoDescription?.trim(),
      imageAlt: imageAlt?.trim(),
      metaKeywords: parsedMetaKeywords,
      focusKeyword: focusKeyword?.trim().toLowerCase(),
      shortDescription: shortDescription?.trim(),
      // Featured spotlight controls
      featuredUntil: featuredUntil ? new Date(featuredUntil) : null,
      isSoldOut: String(isSoldOut).toLowerCase() === 'true'
    };

    // Auto-generate missing SEO fields
    const autoGeneratedSEO = autoGenerateProductSEO(productData);
    const completeProductData = { ...productData, ...autoGeneratedSEO };

    // Generate and ensure unique slug
    const baseSlug = generateProductSlug(title, parsedCategories);
    completeProductData.slug = await ensureUniqueSlug(baseSlug, Product);

    // Validate SEO fields
    const seoValidation = validateSEOFields(completeProductData);
    if (!seoValidation.isValid) {
      console.log('SEO Validation failed:', seoValidation.errors);
      return res.status(400).json({ 
        message: 'SEO validation failed', 
        errors: seoValidation.errors,
        details: {
          seoTitle: `"${completeProductData.seoTitle}" (${completeProductData.seoTitle?.length || 0} chars)`,
          seoDescription: `"${completeProductData.seoDescription}" (${completeProductData.seoDescription?.length || 0} chars)`,
          imageAlt: `"${completeProductData.imageAlt}" (${completeProductData.imageAlt?.length || 0} chars)`
        }
      });
    }

    // Create and save the product
    const newProduct = new Product(completeProductData);
    const savedProduct = await newProduct.save();
    
    // Fire-and-forget: generate AVIF/WebP variants in background
    if (req.file?.path) {
      generateImageVariants(req.file.path).catch(err => 
        console.error('Background image processing failed:', err.message)
      );
    }
    
    console.log('Product saved successfully:', savedProduct.title);
    
    // Clear API cache so product listings update immediately
    apiCache.clear();
    
    const responseData = {
      success: true,
      message: 'Product created successfully',
      product: savedProduct,
      seoScore: savedProduct.seoScore
    };
    
    console.log('Sending response:', responseData);
    res.status(201).json(responseData);
  } catch (error) {
    console.error('=== ADD PRODUCT ERROR ===');
    console.error('Error adding product:', error);
    console.error('Error stack:', error.stack);
    
    const errorResponse = { 
      success: false, 
      message: error.message 
    };
    
    console.log('Sending error response:', errorResponse);
    res.status(500).json(errorResponse);
  }
};
const getProductsByCategory = async (req, res) => {
  try {
    const categoryName = req.params.categoryName.replace(/-/g, " ");
    const page = parseInt(req.query.page) || 1;
    const limit = parseInt(req.query.limit) || 20;
    const skip = (page - 1) * limit;

    // Create case-insensitive regex for category matching
    const categoryRegex = new RegExp(`^${categoryName}$`, "i");
    const query = {
      categories: categoryRegex,
      isActive: true
    };

    // Run queries in parallel for better performance
    const [products, total] = await Promise.all([
      Product.find(query)
        .select('title slug image images categories price seoTitle seoDescription imageAlt shortDescription createdAt featuredUntil isSoldOut soldOutAt')
        .sort({ createdAt: -1 })
        .skip(skip)
        .limit(limit)
        .lean(),
      
      Product.countDocuments(query)
    ]);

    const hasMore = products.length === limit;
    const totalPages = Math.ceil(total / limit);

    // Set cache headers
    res.set({
      'Cache-Control': 'public, max-age=300',
      'X-Total-Count': total,
      'X-Page': page,
      'X-Has-More': hasMore
    });

    res.json({
      products,
      total,
      page,
      limit,
      totalPages,
      hasMore
    });
  } catch (error) {
    console.error('getProductsByCategory error:', error);
    res.status(500).json({ message: error.message });
  }
};
// Get all products (optimized for large datasets)
const getAllProducts = async (req, res) => {
  try {
    const page = parseInt(req.query.page) || 1;
    const limit = parseInt(req.query.limit) || 20;
    const skip = (page - 1) * limit;
    const searchTerm = req.query.search ? req.query.search.trim() : '';
    const categoryFilter = req.query.category ? req.query.category.trim() : '';

    // Admin routes should see ALL products; public routes only active ones.
    const isAdminRoute = req.baseUrl && req.baseUrl.includes('/admin');
    const query = isAdminRoute ? {} : { isActive: { $ne: false } };

    // Apply search filter — searches all name/label fields including seoTitle (shown on product page)
    if (searchTerm) {
      const regex = new RegExp(searchTerm, 'i');
      query.$or = [
        { title: regex },
        { seoTitle: regex },
        { slug: regex },
        { keywords: regex },
        { categories: regex },
        { description: regex }
      ];
    }

    // Apply category filter
    if (categoryFilter) {
      query.categories = categoryFilter;
    }

    const [products, total] = await Promise.all([
      Product.find(query)
        .select('title slug image images categories price keywords description seoTitle seoDescription imageAlt shortDescription isActive createdAt featuredUntil isSoldOut soldOutAt')
        .sort({ createdAt: -1 })
        .skip(skip)
        .limit(limit)
        .lean(),
      Product.countDocuments(query)
    ]);

    // Calculate if there are more pages (for infinite scroll)
    const hasMore = products.length === limit;
    const totalPages = Math.ceil(total / limit);

    // Set aggressive cache headers for API response
    res.set({
      'Cache-Control': 'public, max-age=300', // Cache for 5 minutes
      'X-Total-Count': total, // Useful for clients
      'X-Page': page,
      'X-Has-More': hasMore
    });
    
    res.json({
      products,
      total,
      page,
      limit,
      totalPages,
      hasMore
    });
  } catch (error) {
    console.error('getAllProducts error:', error);
    res.status(500).json({ message: error.message });
  }
};

// Get product by ID or slug
const getProductById = async (req, res) => {
  try {
    const { id } = req.params;
    let product;

    // Try to find by slug first, then by ID
    if (id.match(/^[0-9a-fA-F]{24}$/)) {
      // It's a valid ObjectId
      product = await Product.findById(id);
    } else {
      // It's likely a slug
      product = await Product.findOne({ slug: id.toLowerCase() });
    }

    if (!product) {
      return res.status(404).json({ message: 'Product not found' });
    }

    // Increment view count
    await Product.findByIdAndUpdate(product._id, { $inc: { viewCount: 1 } });

    res.json(product);
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

