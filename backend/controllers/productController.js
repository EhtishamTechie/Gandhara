const updateProduct = async (req, res) => {
  try {
    const { 
      title, 
      keywords, 
      description, 
      price, 
      categories,
      // SEO Fields
      seoTitle,
      seoDescription,
      imageAlt,
      metaKeywords,
      focusKeyword,
      shortDescription,
      // Featured spotlight controls
      featuredUntil,
      isSoldOut
    } = req.body;

    const product = await Product.findById(req.params.id);
    if (!product) {
      return res.status(404).json({ message: 'Product not found' });
    }

    // Parse arrays from JSON strings
    const parsedKeywords = typeof keywords === 'string' ? keywords.split(',').map(keyword => keyword.trim()) : keywords;
    const parsedCategories = typeof categories === 'string' ? JSON.parse(categories) : categories;
    const parsedMetaKeywords = metaKeywords ? (typeof metaKeywords === 'string' ? metaKeywords.split(',').map(k => k.trim()) : metaKeywords) : product.metaKeywords;

    // Prepare updated fields
    const updatedFields = {
      title: title?.trim() || product.title,
      keywords: parsedKeywords || product.keywords,
      description: description?.trim() || product.description,
      price: price ? parseFloat(price) : product.price,
      categories: parsedCategories || product.categories,
      // SEO fields
      // If title changed and seoTitle wasn't explicitly sent, clear it so autoGenerateProductSEO rebuilds from new title
      seoTitle: seoTitle?.trim() || (title?.trim() && title.trim() !== product.title ? '' : product.seoTitle),
      seoDescription: seoDescription?.trim() || product.seoDescription,
      imageAlt: imageAlt?.trim() || product.imageAlt,
      metaKeywords: parsedMetaKeywords,
      focusKeyword: focusKeyword?.trim().toLowerCase() || product.focusKeyword,
      shortDescription: shortDescription?.trim() || product.shortDescription,
      lastSEOUpdate: new Date()
    };

    // Featured spotlight controls (admin-managed)
    if (typeof featuredUntil !== 'undefined') {
      const raw = String(featuredUntil).trim();
      if (raw === '' || raw.toLowerCase() === 'null' || raw.toLowerCase() === 'undefined') {
        updatedFields.featuredUntil = null;
      } else {
        const d = new Date(raw);
        if (!Number.isNaN(d.getTime())) {
          updatedFields.featuredUntil = d;
        }
      }
    }

    if (typeof isSoldOut !== 'undefined') {
      const nextSold = String(isSoldOut).toLowerCase() === 'true';
      updatedFields.isSoldOut = nextSold;
      // Only stamp soldOutAt when transitioning to sold.
      if (nextSold && !product.isSoldOut) {
        updatedFields.soldOutAt = new Date();
      }
      if (!nextSold && product.isSoldOut) {
        updatedFields.soldOutAt = null;
      }
    }

    // Handle slug update if title or categories changed
    if (title && (title !== product.title || parsedCategories)) {
      const baseSlug = generateProductSlug(title, parsedCategories || product.categories);
      updatedFields.slug = await ensureUniqueSlug(baseSlug, Product, req.params.id);
    }

    // Auto-generate missing SEO fields
    const autoGeneratedSEO = autoGenerateProductSEO(updatedFields);
    Object.assign(updatedFields, autoGeneratedSEO);

    // Note: SEO validation is intentionally skipped on updates so admin edits
    // (title, description, categories etc.) are never blocked by SEO length rules.

    // Handle image update and old image deletion
    if (req.file) {
      // Delete old image
      if (product.image && fs.existsSync(product.image)) {
        fs.unlink(product.image, (err) => {
          if (err) {
            console.error('Failed to delete old image:', err);
          } else {
            console.log('Old image deleted:', product.image);
          }
        });
      }
      updatedFields.image = req.file.path;
      
      // Fire-and-forget: generate AVIF/WebP variants for new image
      generateImageVariants(req.file.path).catch(err => 
        console.error('Background image processing failed:', err.message)
      );
    }

    const updatedProduct = await Product.findByIdAndUpdate(req.params.id, updatedFields, { new: true });

    // Clear API cache so listings update immediately
    apiCache.clear();

    res.json({
      success: true,
      message: 'Product updated successfully',
      product: updatedProduct,
      seoScore: updatedProduct.seoScore
    });
  } catch (error) {
    console.error('Error updating product:', error);
    res.status(500).json({ message: error.message });
  }
};

const deleteProduct = async (req, res) => {
  try {
    const product = await Product.findByIdAndDelete(req.params.id);

    if (!product) {
      return res.status(404).json({ message: 'Product not found' });
    }

    // Delete image file from filesystem
    if (product.image && fs.existsSync(product.image)) {
      fs.unlink(product.image, (err) => {
        if (err) {
          console.error('Failed to delete image:', err);
        } else {
          console.log('Image deleted:', product.image);
        }
      });
    }

    res.json({ message: 'Product deleted successfully' });
    
    // Clear API cache
    apiCache.clear();
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

// Server-side search for products (avoids fetching entire catalog client-side)
const searchProducts = async (req, res) => {
  try {
    const { q, page = 1, limit = 20 } = req.query;
    const skip = (parseInt(page) - 1) * parseInt(limit);

    if (!q || !q.trim()) {
      return res.json({ products: [], total: 0, page: 1, limit: parseInt(limit) });
    }

    const searchTerm = q.trim();

    // Use MongoDB text index for efficient search
    const textQuery = {
      $text: { $search: searchTerm },
      isActive: true
    };

    // Fallback: regex search if text search returns no results
    const regexQuery = {
      isActive: true,
      $or: [
        { title: { $regex: searchTerm, $options: 'i' } },
        { description: { $regex: searchTerm, $options: 'i' } },
        { keywords: { $regex: searchTerm, $options: 'i' } },
        { categories: { $regex: searchTerm, $options: 'i' } },
        { shortDescription: { $regex: searchTerm, $options: 'i' } }
      ]
    };

    // Try text search first (faster, uses index)
    let [products, total] = await Promise.all([
      Product.find(textQuery)
        .select('title slug image images categories price seoTitle seoDescription imageAlt shortDescription createdAt keywords featuredUntil isSoldOut soldOutAt')
        .sort({ score: { $meta: 'textScore' } })
        .skip(skip)
        .limit(parseInt(limit))
        .lean(),
      Product.countDocuments(textQuery)
    ]);

    // Fallback to regex if text search found nothing
    if (products.length === 0) {
      [products, total] = await Promise.all([
        Product.find(regexQuery)
          .select('title slug image images categories price seoTitle seoDescription imageAlt shortDescription createdAt keywords featuredUntil isSoldOut soldOutAt')
          .sort({ createdAt: -1 })
          .skip(skip)
          .limit(parseInt(limit))
          .lean(),
        Product.countDocuments(regexQuery)
      ]);
    }

    res.set({
      'Cache-Control': 'public, max-age=60',
      'X-Total-Count': total
    });

    res.json({
      products,
      total,
      page: parseInt(page),
      limit: parseInt(limit),
      totalPages: Math.ceil(total / parseInt(limit)),
      hasMore: products.length === parseInt(limit)
    });
  } catch (error) {
    console.error('searchProducts error:', error);
    res.status(500).json({ message: error.message });
  }
};

